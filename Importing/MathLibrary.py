import math

Val = 3.14
sqrtVal = math.sqrt(Val)
print(sqrtVal)#sqrtVal^2

expVal = math.exp(Val)#e^(Val)

print(expVal)

factVal = math.factorial(math.floor(Val))#5! = 5*4*3*2*1

print(factVal)

sinVal = math.sin(Val)
print(sinVal)

floorVal = math.floor(Val)
print(floorVal)

ceilingVal = math.ceil(Val)
print(ceilingVal)
